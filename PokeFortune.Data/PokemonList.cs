		private static void AddNewPokemon(this List<Pokemon> list, int id, string name, PokemonType firstPokemonType, PokemonType secondPokemonType = PokemonType.None, LegendaryType legendaryType = LegendaryType.None, bool hasFemale = false, string formName = "")
		{
			list.Add(new Pokemon(id, name, firstPokemonType, secondPokemonType, legendaryType, hasFemale) { FormName = formName });
		}

		private static void AddFromLastNewForm(this List<Pokemon> list, int subId, string formName, string additionalUri, PokemonType firstType, PokemonType secondType, GenerationType generation, FormType formType = FormType.Other)
		{
			var lastPoke = list?.LastOrDefault();

			if (lastPoke != null)
				_ = new PokeForm(lastPoke, subId, formName, additionalUri, firstType, secondType, generation, formType);
		}

